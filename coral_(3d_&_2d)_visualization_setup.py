import PIL.Image
import matplotlib.pyplot as plt
import os
import cv2
import numpy as np

folder_path = '/content/drive/MyDrive/Coral AI/drive-download-20260309T231545Z-3-001/'
file_names = ['C1 (T).JPG', 'C8 (T).JPG', 'SX-3.JPG', 'SX-5.JPG']

fig, axes = plt.subplots(4, 4, figsize=(20, 16))

for i, name in enumerate(file_names):
    img_path = os.path.join(folder_path, name)

    if os.path.exists(img_path):
        # Load image
        img_bgr = cv2.imread(img_path)
        img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

        # Convert to grayscale
        gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY)

        # Blur slightly to reduce noise
        gray_blur = cv2.GaussianBlur(gray, (5, 5), 0)

        # Threshold grayscale image
        # Try changing 140 to 120 or 160 depending on results
        _, mask = cv2.threshold(gray_blur, 140, 255, cv2.THRESH_BINARY)

        # Clean mask
        kernel = np.ones((3, 3), np.uint8)
        mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN, kernel)
        mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel)

        # Make overlay in blue
        overlay = img_rgb.copy()
        overlay[mask > 0] = [0, 0, 255]   # blue in RGB
        blended = cv2.addWeighted(img_rgb, 0.7, overlay, 0.3, 0)

        # Plot original
        axes[0, i].imshow(img_rgb)
        axes[0, i].set_title(f"{name}\nOriginal")
        axes[0, i].axis('off')

        # Plot grayscale
        axes[1, i].imshow(gray, cmap='gray')
        axes[1, i].set_title("Grayscale")
        axes[1, i].axis('off')

        # Plot mask
        axes[2, i].imshow(mask, cmap='gray')
        axes[2, i].set_title("Mask")
        axes[2, i].axis('off')

        # Plot overlay
        axes[3, i].imshow(blended)
        axes[3, i].set_title("Overlay")
        axes[3, i].axis('off')

    else:
        for row in range(4):
            axes[row, i].text(0.5, 0.5, f'File not found:\n{name}', ha='center', va='center')
            axes[row, i].axis('off')

plt.tight_layout()
plt.show()

